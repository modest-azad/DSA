#include<iostream>
using namespace std;

int main()
{
    int a = 'A';// a will store ASCII value of A = 65
    cout<<a<<endl; //output: 65
    

    char ch = 64;
    cout<<ch<<endl; // output: @, because 64 mapped as a character in ASCII as @
     
    return 0;
}