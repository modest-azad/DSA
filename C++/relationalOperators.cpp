// #Relational Operators

#include<iostream>
using namespace std;

int main()
{
    int a = 5, b = 8;

    bool first = (a>b);
    cout<<first<<endl;
    
    bool second = (a<b);
    cout<<second<<endl;

    bool third = (a>=b);
    cout<<third<<endl;

    bool forth = (a==b);
    cout<<forth<<endl;

    bool sixth = (a!=b);
    cout<<sixth<<endl;

    // NOTES :0 (false), 1(true )
    
    return 0;
}